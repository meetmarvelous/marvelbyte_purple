                <h3><strong>NEW UNIFIED USSD SHORTCODES LIST FOR ALL NIGERIAN NETWORKS</strong></h3>

                <p> The Unified  <a href="#">USSD codes</a> for all Nigerian mobile networks are shown below:</p>

                <ul class="check">
                  <li>Call Centre(Customer Care): 300</li>
                  <li>Borrow Services: *303#</li>
                  <li>Stop Service: *305#</li>
                  <li>Check Balance: *310#</li>
                  <li>Credit Recharge: *311#</li>
                  <li>Data Plan: *312#</li>
                  <li>Data plan balance: *323#</li>
                  <li>Share Services: *321#</li>
                </ul>

                <p>Since the typical Nigerian mobile phone user has multiple sim cards, the adoption of universal USSD codes will remove the inconvenience of having to memorize numerous USSD codes of different networks for everyday transactions.</p>

