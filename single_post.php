              <div class="pp">
                <p>You may have noticed that using the *556# to check your airtime balance on the MTN network is no longer possible. This is because the Nigerian Communication Commission has ordered and mandated that all network operators in Nigeria adopt and use a set of new Unified(the same) USSD shortcodes for such operations, and MTN has taken the lead in rolling out the new codes on their network.</p>

                <p>Stakeholders in the telecom sector will soon start putting consistent shortcodes into practice, five years after the Nigerian Communications Commission (NCC) issued a report on them. Customers of various cell providers and banks can use the same USSD codes to check their airtime balance, purchase airtime, and even conduct financial transactions thanks to harmonised shortcodes. The main justification for harmonization is that it will make regulations easier to remember and more unified.</p>

                <p>The Association of Licenced Telecommunications Operators of Nigeria (ALTON) estimates that the transition to the new set of codes will be complete by May 17th, 2023, putting an end to the use of the previous shortcode system. The only method for sending commands on mobile devices will then continue to be the new shortcodes that have been offered. Shortcodes 300 for call centers, 303 for airtime borrowing services, 310 for balance checking, 311 for credit recharge, 312 for data plans, and 2442 for porting services are among the harmonised shortcodes that have been suggested.</p>

                <p><img src="./assets/images/blog/codes.png" class="float-left" width="380" alt="">On the MTN network, if you dial the *556# command as of May 17, 2023, you will receive a message saying that your dial was incorrect and asking you to select the command you intended from a choice of seven options, as shown in the screenshot below.</p>

                <p>On the MTN network, if you dial the *556# command as of May 17, 2023, you will receive a message saying that your dial was incorrect and asking you to select the command you intended from a choice of seven options, as shown in the screenshot below.</p>

                <p>Therefore, you should use the new Unified code *310#, which is the list of the unified codes for other networks activities, to check your account balance on MTN and other networks in Nigeria (Airtel, GLO, and 9mobile).</p>

